//logo action
function logoWebbab() {
    var self = this;
    self.wrapperLogo = $(".logo-webbab");
    self.svgLogo = $(".logo-webbab svg");
    self.isoLogo = $(".logo-webbab .rhombus");
    self.wordLogo = $(".logo-webbab .word");

    self.timer = 0.9;


    self.init = function() {

        if (!self.isDevice) {
            self.setObj();
            self.animation();
        } else {
            self.wrapperLogo.empty().append(self.mobileLogo);
        }

    };

    self.setObj = function() {

        TweenMax.set(self.isoLogo, {
            attr: {
                rx: 50,
                ry: 50
            },
            transformOrigin: "50% 50%",
            rotation: 120,
            scale: 0,
            autoAlpha: 1
        });
    };

    self.animation = function() {

        //TweenMax.ticker.fps(60);

        // SCENES
        function rhombusRotation() {
            var tl = new TimelineMax();
            tl.to(self.isoLogo, 0.6, {
                    autoAlpha: 1,
                    scale: 1,
                    ease: Power1.easeIn
                })
                .to(self.isoLogo, 0.6, {
                    attr: {
                        rx: 0,
                        ry: 0
                    },
                    rotation: 360,
                    ease: Power1.easeOut
                });
            return tl;
        }

        function textAnimation() {
            var tl = new TimelineMax();
            tl.staggerFromTo(self.wordLogo, 1.5, {
                autoAlpha: 0,
                scale: 0.8,
                x: -20,
                transformOrigin: "left center",
                ease: Elastic.easeOut.config(1, 0.3)
            }, {
                autoAlpha: 1,
                scale: 1,
                x: 0,
                ease: Elastic.easeOut.config(1, 0.5)
            }, 0.05);
            return tl;
        }

        // MASTER SCENES
        var master = new TimelineMax({
            // LOOP
            // repeat: -1,
            // repeatDelay: 0.5,
            yoyo: true

        });
        master //.add(pulseCircle(), "scene1")
            .add(rhombusRotation(), "scene2")
            .add(textAnimation(), "-=0.5", "scene5");
        master.timeScale(self.timer);
    };
}
</script>

